part of 'app_resources.dart';

class IconSizes {
  static double get iconSizeXXXS {
    return SizeConfig.safeBlockHorizontal * 2;
  }

  static double get iconSizeXXS {
    return SizeConfig.safeBlockHorizontal * 3;
  }

  static double get iconSizeXS {
    return SizeConfig.safeBlockHorizontal * 4;
  }

  static double get iconSizeS {
    return SizeConfig.safeBlockHorizontal * 5;
  }

  static double get iconSizeM {
    return SizeConfig.safeBlockHorizontal * 6;
  }

  static double get iconSizeL {
    return SizeConfig.safeBlockHorizontal * 7;
  }

  static double get iconSizeXL {
    return SizeConfig.safeBlockHorizontal * 8;
  }
}
